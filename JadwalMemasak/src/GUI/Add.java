    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        try {
            String nm = nama.getText();
            String jn = (String) jenis.getSelectedItem();
            String hr = (String) hari.getSelectedItem();
//            String iu = id.getText();
         
            java.util.Date utilDate = tanggal.getDate();
            java.sql.Date sqlDate = null;
            if (utilDate != null) {
                sqlDate = new java.sql.Date(utilDate.getTime());
                java.util.Date tg = tanggal.getDate();
            } else {
                JOptionPane.showMessageDialog(null, "Pilih tanggal terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int bg = 0;
            
            try {
            bg = Integer.parseInt(budget.getText());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Budget harus diisi dengan benar", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;  // Exit the method if parsing fails.
            }
            
            if (nm.isEmpty() || jn.isEmpty() || hr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Semua kolom harus diisi.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            } else {
                jadwalmemasak jadwal = new jadwalmemasak(nm, jn, hr, sqlDate, bg, this.pengguna.ID_User);
                jadwal.setNama_Makanan(nm);
                jadwal.setJenis_Makanan(jn);
                jadwal.setHari_Memasak(hr);
                jadwal.setTanggal(sqlDate);
                jadwal.setBudget(bg);
                jadwal.setPENGGUNA_ID_User(this.pengguna.ID_User);
                
                boolean createSuccess = jadwal.tambahjadwalmemasak();

            if (createSuccess) {
                JOptionPane.showMessageDialog(null, "Jadwal berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menambahkan data Jilbab.", "Error", JOptionPane.ERROR_MESSAGE);
        }
            nama.setText("");
            jenis.setSelectedItem(null);
            hari.setSelectedItem(null);
            tanggal.setDate(null);
            budget.setText("");
//            id.setText(this.pengguna.ID_User);
            }
            
        } catch (Exception e) {
            e.printStackTrace(); // Cetak detail pengecualian untuk membantu identifikasi masalah
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_addActionPerformed

